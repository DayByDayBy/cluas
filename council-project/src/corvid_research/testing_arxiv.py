import requests
import feedparser

KEYWORDS = [
    'corvid', 'crow', 'raven', 'corvus', 'jay', 'magpie',
    'jackdaw', 'rook', 'chough', 'nutcracker'
]

def fetch_one_arxiv_example():
    query = " OR ".join(KEYWORDS)
    url = (
        "https://export.arxiv.org/api/query?"
        f"search_query=all:({query})&"
        "start=0&max_results=1&sortBy=lastUpdatedDate&sortOrder=descending"
    )

    response = requests.get(url)
    feed = feedparser.parse(response.text)

    if not feed.entries:
        return None

    entry = feed.entries[0]

    return {
        "title": entry.title,
        "abstract": entry.summary,
        "authors": [a.name for a in entry.authors],
        "published": entry.published,
        "link": entry.link,
    }

result = fetch_one_arxiv_example()
print(result)




